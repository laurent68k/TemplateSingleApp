import Foundation
import RxSwift

class CollectionViewModel {
    
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    private (set) var items = Variable<[String]>( ["Cell a","Cell b","Cell c","Cell d","Cell e","Cell f"])
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    init() {
        
    }
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    var numberOfItems : Int {
        
        return self.items.value.count
    }
    
    func textExample(atIndex index:IndexPath) -> String {
        
        return self.items.value[index.row]
    }
    
    /// ---------------------------------------------------------------------------------------------------------------------------------------------
    /// ---------------------------------------------------------------------------------------------------------------------------------------------

}
